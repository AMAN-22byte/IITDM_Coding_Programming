# count - Occurance
my_fav = [2,3,5,7,11,97]
your_fav = [8,27,26,10,25,1,7,70]
My_Series_1 =  pa.Series(my_fav)
My_Series_2 =  pa.Series(your_fav)

print(f"Count of My_Series_1: {My_Series_1.count()};Count of My_Series_2: {My_Series_2.count()}")
